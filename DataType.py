# Types of datatypes
numbers = 20 #Integer
second = 56.78 # Float
text = "Nurdin" # String

IsPythonInteresting = True # Boolean

print(numbers)
print(second)
print(text)
print(IsPythonInteresting)

#Determining the datatype
print(type(numbers))
print(type(second))
print(type(text))
print(type(IsPythonInteresting))

#typecasting is the process of converting from one datatype to another
print(float(numbers))
print(int(second))
